package com.smart.drools;

public class Ratio {
	private String numeratorId;
	private String denominatorId;
	private float reflo;
	private float refhi;
	
	public String getNumeratorId() {
		return numeratorId;
	}
	public void setNumeratorId(String numeratorId) {
		this.numeratorId = numeratorId;
	}
	public String getDenominatorId() {
		return denominatorId;
	}
	public void setDenominatorId(String denominatorId) {
		this.denominatorId = denominatorId;
	}
	public float getReflo() {
		return reflo;
	}
	public void setReflo(float reflo) {
		this.reflo = reflo;
	}
	public float getRefhi() {
		return refhi;
	}
	public void setRefhi(float refhi) {
		this.refhi = refhi;
	}
	
	
	
}
